/******* while, do-while loop ************/

let index = 0;

while (index <= 10) {
    // console.log(index);
    index += 2;
}


const fruits = ['apple', 'mango', 'banana'];
let i = 0;
while (i < fruits.length) {
    // console.log(fruits[i]);
    i++;
}

let score = 12;

do {
    console.log(`score: ${score}`);
    score++;
} while (score <= 10);
